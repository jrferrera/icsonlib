			<p>
			The first time Computer Science was recognized as a discipline in UPLB was in 1982 when the
			Institute of Mathematical Sciences and Physics (IMSP) was established with the Division of Computer
			Science as one of its divisions. Dr. Ann Inez N. Gironella was the first director of IMSP and Prof.
			Wilfredo Cabezon was the first Division Head of the Division of Computer Science. IMSP was
			established when Dr. Emil Q. Javier was the Chancellor of UPLB.
			</p>
			<p>
			Together with the establishment of the Division of Computer Science was the institution of the BS
			Computer Science (BSCS) curriculum in UPLB. Hence, the BS Computer Science Program was
			officially instituted in 1982 but the first students in the program were admitted in 1983.
			</p>
			<p>
			During the initial implementation of the BSCS program, three professors and instructors in Statistics
			handled the computer science courses. These three are: Dr. Manuel M. Manuel, Prof. Wilfredo E.
			Cabezon, and Dr. Eliezer A. Albacea (Instructor then).
			</p>

			<p>
			With the help of the Australian government through the International Development Program (IDP) of
			Australian Universities and Colleges, the staff of the Division grew in number from initially three to
			about ten with graduate degrees in Computer Science.
			</p>
			<p>	
			In 1988, the Division instituted the Master of Science in Computer Science. This became very popular
			among the teaching staff of the Division.
			</p>
			<p>
			In January 26, 1995, the UP Board of Regents separated the Division of Computer Science from IMSP
			and merged this with the existing Los Banos Computer Center to form the Institute of Computer
			Science (ICS). This happened when Dr. Eliezer A. Albacea was the Head of the Division, Dr. Ruben
			Villareal was the Chancellor of UPLB and Dr. Emil Q. Javier was the President of UP. Dr. Eliezer A.
			Albacea became the first Director of ICS.
			</p>
			<p>
			A year later, the Institute was designated by the Board of Regents as a pilot institute to implement the
			concept of a System Academic Program. Dr. Eliezer A. Albacea was designated System Director for
			the UP System Computer Science Program. ICS is instrumental in the institution of BSCS programs in
			UP Manila, UP in the Visayas (Cebu, Tacloban, and Iloilo), UP College Baguio, and UP Mindanao.
			In 1996, ICS instituted the Diploma in Computer Science both in residential and distance education
			modes while in 1998, the Ph.D. in Computer Science was instituted. In 2002, the Master of Information
			Technology was instituted.
			</p>
